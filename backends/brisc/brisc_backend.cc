bool
BriscBackend::is_dff(const Cell* cell) const
{
    return reg_ct.count(cell->type) && cell->hasPort("\\Q");
}

/// @brief Count comb / dff cells in design's top_module
/// @return <comb count, dff count>
std::pair<int, int>
BriscBackend::count_cells(Design* design)
{
    int comb = 0, dff = 0;

    for (Cell* cell : design->top_module()->cells()) {
        if (nullptr == cell)
            continue;
        if (is_dff(cell)) {
            ++dff;
        }
        else {
            ++comb;
        }
    }
    return make_pair(comb, dff);
}

void
BriscBackend::
dump_module(std::ostream& f, std::string indent, Module* mod)
{
    reg_wires.clear();
    reset_auto_counter(mod);
    active_module = mod;
    active_sigmap.set(active_module);
    active_initdata.clear();

    // Get initializaton information
    for (auto wire : mod->wires()) {
        if (wire->attributes.count("\\init")) {
            SigSpec sig = active_sigmap(wire);
            Const val = wire->attributes.at("\\init");
            for (int i = 0; i < GetSize(sig) && i < GetSize(val); i++) {
                if (val[i] == State::S0 || val[i] == State::S1) {
                    active_initdata[sig[i]] = val[i];
                }
            }
        }
    }

    // Check for processes
    if (mod->has_processes()) {
		log_warning("Module %s contains unmapped RTLIL processes. RTLIL processes\n"
				"can't always be mapped directly to Verilog always blocks. Unintended\n"
				"changes in simulation behavior are possible! Use \"proc\" to convert\n"
				"processes to logic networks and registers.\n", log_id(mod));
        log_abort();
    }

    // Dump processes
    f << stringf("\n");
	// for (auto it = mod->processes.begin(); it != mod->processes.end(); ++it) {
	// 	dump_process(f, indent + "  ", it->second, true);
    // }

    /// Impossible to reach --- noexpr is static constexpr true
    if (!noexpr) {
        log_abort();
    }

    dump_attributes(f, indent, mod->attributes, '\n', /*modattr=*/true);
    f << stringf("%s" "module %s\n(", indent.c_str(),
                 id(mod->name, false).c_str());

    // Dump input / output ports
    bool keep_running = true;
    static constexpr char _inport[] = "input ";
    static constexpr char _outport[] = "output logic ";
    for (int port_id = 1; keep_running; port_id++) {
        keep_running = false;
        for (Wire* wire : mod->wires()) {
            if (wire->port_id == port_id) {
                if (port_id != 1) {
                    f << stringf(",");
                }
                std::string range = "";
                if (wire->width != 1) {
                    if (wire->upto) {
                        range = stringf("[%d:%d]", wire->start_offset,
                                        wire->width-1 + wire->start_offset);
                    }
                    else {
                        range = stringf("[%d:%d]",
                                        wire->width-1 + wire->start_offset,
                                        wire->start_offset);
                    }
                }
                f << stringf("\n%s  %s %s %s"
                        ,indent.c_str()
                        ,wire->port_input && wire->port_output ?
                                                   "inout logic " :
                            wire->port_input  ?              _inport :
                            wire->port_output ?             _outport :
                                  "error --- unknown port direction"
                        ,range.c_str()
                        ,id(wire->name).c_str());
                keep_running = true;
                continue;
            }
        }
    }

    // Dump interface ports
    f << stringf("\n%s  ,%s._cell %s [%d]"
                 ,indent.c_str()
                 ,comb_itf
                 ,comb_itf_id
                 ,cell_count_comb
                 );
    f << stringf("\n%s  ,%s._cell %s [%d]"
                 ,indent.c_str()
                 ,dff_itf
                 ,dff_itf_id
                 ,cell_count_dff
                 );
	f << stringf("%s\n);\n", indent.c_str());

    for (auto wire : mod->wires()) {
        if (wire->port_input || wire->port_output)
            continue;
        dump_wire(f, indent + "  ", wire);
    }

    for (auto it = mod->memories.begin(); it != mod->memories.end(); ++it) {
        log_error("BRISC does not support memories...\n");
        log_abort();
    }

    for (Cell* cell : mod->cells()) {
        dump_cell(f, indent + " ", cell);
    }

    for (auto conn : mod->connections()) {
        dump_conn(f, indent + "  ", conn.first, conn.second);
    }

	f << stringf("%s" "endmodule\n", indent.c_str());
	active_module = NULL;
	active_sigmap.clear();
	active_initdata.clear();
}

void
BriscBackend::reset_auto_counter(Module *mod)
{
    auto_name_map.clear();
    auto_name_counter = auto_name_offset = 0;

    reset_auto_counter_id(mod->name, false);

    for (Wire* wire : mod->wires()) {
        reset_auto_counter_id(wire->name, true);
    }

    for (Cell* cell : mod->cells()) {
        reset_auto_counter_id(cell->name, true);
        reset_auto_counter_id(cell->type, false);
    }

    log_assert(!mod->has_processes());
    // Boilerplate anyway
	for (auto it = mod->processes.begin(); it != mod->processes.end(); ++it) {
		reset_auto_counter_id(it->second->name, false);
    }

    auto_name_digits = 1;
    for (size_t i = 10; i < auto_name_offset + auto_name_map.size(); i *= 10) {
        auto_name_digits++;
    }

    if (verbose) {
        for (auto it : auto_name_map) {
            log("  renaming `%s' to `%s_%0*d_'.\n", it.first.c_str(),
                    auto_prefix.c_str(),
                    auto_name_digits, auto_name_offset + it.second);
        }
    }
}

void
BriscBackend::reset_auto_counter_id(IdString id, bool may_rename)
{
    const char* str = id.c_str();

    if constexpr (!norename) {
        if (str[0] == '$' && may_rename && !norename) {
            auto_name_map[id] = auto_name_counter++;
        }
        log_abort();
    }

    if (str[0] != '\\' || str[1] != '_' || str[2] == '\0') {
        return;
    }

    for (int i = 2; str[i] != '\0'; ++i) {
        if (str[i] == '_' && str[i+1] == '\0') {
            continue;
        }
        if (str[i] < '0' || str[i] > '9')
            return;
    }

    int num = atoi(str+2);
    if (num >= auto_name_offset)
        auto_name_offset = num + 1;
}

