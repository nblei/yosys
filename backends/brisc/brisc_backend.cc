void
BriscBackend::dump_attributes(
        std::ostream& f,
        std::string indent,
        dict<IdString, Const> &attributes,
        char term,
        bool modattr,
        bool regattr,
        bool as_comment)
{
    if (noattr)
        return;
    as_comment |= attr2comment;

    for (auto it : attributes) {
        if (it.first == "\\init" && regattr)
            continue;

        f << stringf("%s" "%s %s", indent.c_str(), as_comment ? "/*" : "(*",
                id(it.first).c_str());
        f << stringf(" = ");
        if (modattr && (it.second == State::S0 || it.second == Const(0))) {
            f << stringf( " 0 ");
        }
        else if (modattr && (it.second == State::S1 || it.second == Const(0))) {
            f << stringf(" 1 ");
        }
        else {
            dump_const(f, it.second, -1, 0, false, as_comment);
        }
        f << stringf(" %s%c", as_comment ? "*/" : "*)", term);
    }

}

std::string
BriscBackend::id(IdString internal_id, bool may_rename)
{
    const char* str = internal_id.c_str();
    bool do_escape = false;

    if (may_rename && auto_name_map.count(internal_id) != 0) {
        return stringf("%s_%0*d_", auto_prefix.c_str(), auto_name_digits,
                auto_name_offset + auto_name_map[internal_id]);
    }

    if (*str == '\\')
        str++;

    if ('0' <= *str && *str <= '9')
        do_escape = true;

    for (int i = 0; str[i] != '\0'; ++i) {
        if ('0' <= *str && *str <= '9')
            continue;
		if ('a' <= str[i] && str[i] <= 'z')
			continue;
		if ('A' <= str[i] && str[i] <= 'Z')
			continue;
		if (str[i] == '_')
			continue;
        do_escape = true;
        break;
    }

    if (keywords.count(str))
        do_escape = true;

    if (do_escape)
        return "\\" + std::string(str) + " ";
    return std::string(str);
}

void
BriscBackend::dump_const(
        std::ostream& f,
        const Const& data,
        int width,
        int offset,
        bool no_decimal,
        bool escape_comment)
{
    bool set_signed = (data.flags & RTLIL::CONST_FLAG_SIGNED);

    if (width < 0)
        width = data.bits.size() - offset;
    if (width == 0) {
		// See IEEE 1364-2005 Clause 5.1.14.
		f << "{0{1'b0}}";
		return;
	}
    
    if (nostr) {
        goto dump_hex;
    }

    if ((data.flags & RTLIL::CONST_FLAG_STRING) == 0 ||
            width != (int)data.bits.size()) {
        if (width == 32 && !no_decimal && !nodec) {
            int32_t val = 0;
            for (int i = offset+width-1; i >= offset; i--) {
				log_assert(i < (int)data.bits.size());
				if (data.bits[i] != State::S0 && data.bits[i] != State::S1)
					goto dump_hex;
				if (data.bits[i] == State::S1)
					val |= 1 << (i - offset);
            }
            if (decimal) {
                f << stringf("%d", val);
            }
            else if (set_signed && val < 0) {
                f << stringf("-32'sd%u", -val);
            }
            else {
                f << stringf("32'%sd%u", set_signed ? "s" : "", val);
            }
        }
        else {
dump_hex:
            if (nohex) {
                goto dump_bin;
            }
            vector<char> bin_digits, hex_digits;
            for (int i = offset; i < offset+width; ++i) {
                log_assert(i < (int)data.bits.size());
                switch (data.bits[i]) {
                    case State::S0: bin_digits.push_back('0'); break;
                    case State::S1: bin_digits.push_back('1'); break;
                    case State::Sx: bin_digits.push_back('x'); break;
                    case State::Sz: bin_digits.push_back('z'); break;
                    case State::Sa: bin_digits.push_back('?'); break;
                    case State::Sm:
                        log_error("Found marker state in final netlist");
                }
            }

			if (GetSize(bin_digits) == 0) {
				goto dump_bin;
            }

			while (GetSize(bin_digits) % 4 != 0) {
				if (bin_digits.back() == '1') {
					bin_digits.push_back('0');
                }
				else {
					bin_digits.push_back(bin_digits.back());
                }
            }

            for (int i = 0; i < GetSize(bin_digits); i+= 4)
            {
				char bit_3 = bin_digits[i+3];
				char bit_2 = bin_digits[i+2];
				char bit_1 = bin_digits[i+1];
				char bit_0 = bin_digits[i+0];
				if (bit_3 == 'x' || bit_2 == 'x' || bit_1 == 'x' ||bit_0 == 'x') {
					if (bit_3 != 'x' || bit_2 != 'x' || bit_1 != 'x' || bit_0 != 'x')
						goto dump_bin;
					hex_digits.push_back('x');
					continue;
				}
				if (bit_3 == 'z' || bit_2 == 'z' || bit_1 == 'z' || bit_0 == 'z') {
					if (bit_3 != 'z' || bit_2 != 'z' || bit_1 != 'z' || bit_0 != 'z')
						goto dump_bin;
					hex_digits.push_back('z');
					continue;
				}
				if (bit_3 == '?' || bit_2 == '?' || bit_1 == '?' || bit_0 == '?') {
					if (bit_3 != '?' || bit_2 != '?' || bit_1 != '?' || bit_0 != '?')
						goto dump_bin;
					hex_digits.push_back('?');
					continue;
				}
				int val = 8*(bit_3 - '0') + 4*(bit_2 - '0') + 2*(bit_1 - '0') +
                            (bit_0 - '0');
				hex_digits.push_back(val < 10 ? '0' + val : 'a' + val - 10);
            }

			f << stringf("%d'%sh", width, set_signed ? "s" : "");
			for (int i = GetSize(hex_digits)-1; i >= 0; i--) {
				f << hex_digits[i];
            }
        }

		if (0) {
	dump_bin:
			f << stringf("%d'%sb", width, set_signed ? "s" : "");
			if (width == 0)
				f << stringf("0");
			for (int i = offset+width-1; i >= offset; i--) {
				log_assert(i < (int)data.bits.size());
				switch (data.bits[i]) {
				case State::S0: f << stringf("0"); break;
				case State::S1: f << stringf("1"); break;
				case RTLIL::Sx: f << stringf("x"); break;
				case RTLIL::Sz: f << stringf("z"); break;
				case RTLIL::Sa: f << stringf("?"); break;
				case RTLIL::Sm: log_error("Found marker state in final netlist.");
				}
			}
		}
    }
    else {
		if ((data.flags & RTLIL::CONST_FLAG_REAL) == 0)
			f << stringf("\"");
		std::string str = data.decode_string();
		for (size_t i = 0; i < str.size(); i++) {
			if (str[i] == '\n')
				f << stringf("\\n");
			else if (str[i] == '\t')
				f << stringf("\\t");
			else if (str[i] < 32)
				f << stringf("\\%03o", str[i]);
			else if (str[i] == '"')
				f << stringf("\\\"");
			else if (str[i] == '\\')
				f << stringf("\\\\");
			else if (str[i] == '/' && escape_comment && i > 0 && str[i-1] == '*')
				f << stringf("\\/");
			else
				f << str[i];
		}
		if ((data.flags & RTLIL::CONST_FLAG_REAL) == 0)
			f << stringf("\"");
    }
}

void
BriscBackend::dump_wire(std::ostream& f, std::string indent, Wire* wire)
{
    dump_attributes(f, indent, wire->attributes, '\n',
            /*modattr=*/false, /*regattr=*/reg_wires.count(wire->name));

    std::string range = "";
    if (wire->width != 1) {
        if (wire->upto) {
			range = stringf(" [%d:%d]", wire->start_offset,
                    wire->width - 1 + wire->start_offset);
        }
        else {
			range = stringf(" [%d:%d]", wire->width - 1 + wire->start_offset,
                    wire->start_offset);
        }
    }
	if (wire->port_input && !wire->port_output) {
		f << stringf("%s" "input%s %s;\n", indent.c_str(), range.c_str(),
                     id(wire->name).c_str());
    }
	if (!wire->port_input && wire->port_output) {
		f << stringf("%s" "output%s %s;\n", indent.c_str(), range.c_str(),
                     id(wire->name).c_str());
    }
	if (wire->port_input && wire->port_output) {
		f << stringf("%s" "inout%s %s;\n", indent.c_str(), range.c_str(),
                     id(wire->name).c_str());
    }
	if (reg_wires.count(wire->name)) {
		f << stringf("%s" "reg%s %s", indent.c_str(), range.c_str(),
                     id(wire->name).c_str());
		if (wire->attributes.count("\\init")) {
			f << stringf(" = ");
			dump_const(f, wire->attributes.at("\\init"));
		}
		f << stringf(";\n");
	}
    else if (!wire->port_input && !wire->port_output) {
		f << stringf("%s" "wire%s %s;\n", indent.c_str(), range.c_str(), id(wire->name).c_str());
    }
}

void
BriscBackend::dump_cell(std::ostream& f, std::string indent, Cell* cell)
{
	if (cell->type[0] == '$' && !noexpr) {
        log_abort(); // Impossible to hit
		// if (dump_cell_expr(f, indent, cell))
		// 	return;
	}
	dump_attributes(f, indent, cell->attributes);
	f << stringf("%s" "%s", indent.c_str(),
                 id(cell->type, /*mayrename*/ false).c_str());

    if (!defparam && cell->parameters.size() > 0) {
        log_error("Expected Zero parameters\n");
        log_error("To implement, see verilog_backend.cc:dump_cell\n");
        log_abort();
    }

    std::string cell_name = cellname(cell);
	if (cell_name != id(cell->name))
		f << stringf(" %s /* %s */ (", cell_name.c_str(), id(cell->name).c_str());
	else
		f << stringf(" %s (", cell_name.c_str());

    // Write Ports
	bool first_arg = true;
	std::set<RTLIL::IdString> numbered_ports;
	for (int i = 1; true; i++) {
		char str[16];
		snprintf(str, 16, "$%d", i);
		for (auto it = cell->connections().begin(); it != cell->connections().end(); ++it) {
			if (it->first != str)
				continue;
			if (!first_arg)
				f << stringf(",");
			first_arg = false;
			f << stringf("\n%s  ", indent.c_str());
			dump_sigspec(f, it->second);
			numbered_ports.insert(it->first);
			goto found_numbered_port;
		}
		break;
	found_numbered_port:;
	}
	for (auto it = cell->connections().begin(); it != cell->connections().end(); ++it) {
		if (numbered_ports.count(it->first))
			continue;
		if (!first_arg)
			f << stringf(",");
		first_arg = false;
		f << stringf("\n%s  .%s(", indent.c_str(), id(it->first).c_str());
		if (it->second.size() > 0)
			dump_sigspec(f, it->second);
		f << stringf(")");
	}

    // Write interface ports
    static int comb_offset = 0;
    static int dff_offset = 0;
    bool dff = is_dff(cell);
    f << stringf(",\n%s  ", indent.c_str());
    f << stringf(".itf(%s[%d])",
            dff ? dff_itf_id : comb_itf_id,
            dff ? dff_offset++ : comb_offset++); // Note: Ternary operator is correct
    f << stringf("\n%s);\n", indent.c_str());

	if constexpr (defparam) {
        log_abort();
	}

	if (siminit && reg_ct.count(cell->type) && cell->hasPort("\\Q")) {
		std::stringstream ss;
		dump_reg_init(ss, cell->getPort("\\Q"));
		if (!ss.str().empty()) {
			f << stringf("%sinitial %s.Q", indent.c_str(), cell_name.c_str());
			f << ss.str();
			f << ";\n";
		}
	}

}

void
BriscBackend::dump_sigspec(std::ostream &f, const RTLIL::SigSpec &sig)
{
	if (GetSize(sig) == 0) {
		f << "\"\"";
		return;
	}
	if (sig.is_chunk()) {
		dump_sigchunk(f, sig.as_chunk());
	} else {
		f << stringf("{ ");
		for (auto it = sig.chunks().rbegin(); it != sig.chunks().rend(); ++it) {
			if (it != sig.chunks().rbegin())
				f << stringf(", ");
			dump_sigchunk(f, *it, true);
		}
		f << stringf(" }");
	}
}

void
BriscBackend::dump_sigchunk(std::ostream &f, const RTLIL::SigChunk &chunk,
                            bool no_decimal)
{
    // If Const
    if (chunk.wire == nullptr) {
		dump_const(f, chunk.data, chunk.width, chunk.offset, no_decimal);
        return;
    }

    if (chunk.width == chunk.wire->width && chunk.offset == 0) {
        f << stringf("%s", id(chunk.wire->name).c_str());
    }
    else if (chunk.width == 1) {
        if (chunk.wire->upto) {
            f << stringf("%s[%d]", id(chunk.wire->name).c_str(),
                        (chunk.wire->width - chunk.offset - 1) +
                        chunk.wire->start_offset);
        }
        else {
            f << stringf("%s[%d]", id(chunk.wire->name).c_str(),
                         chunk.offset + chunk.wire->start_offset);
        }
    }
    else {
        if (chunk.wire->upto) {
            f << stringf(
                    "%s[%d:%d]", id(chunk.wire->name).c_str(),
                    (chunk.wire->width - (chunk.offset + chunk.width - 1) - 1)+
                    chunk.wire->start_offset,
                    (chunk.wire->width - chunk.offset - 1) +
                    chunk.wire->start_offset
                    );
        }
        else
            f << stringf(
                    "%s[%d:%d]", id(chunk.wire->name).c_str(),
                    (chunk.offset + chunk.width - 1) + chunk.wire->start_offset,
                    chunk.offset + chunk.wire->start_offset
                    );
    }

}

std::string
BriscBackend::cellname(Cell* cell)
{
    if constexpr (!norename) {
        log_abort();
    }
	else
	{
// no_special_reg_name:
		return id(cell->name).c_str();
	}
}

bool
BriscBackend::is_reg_wire(RTLIL::SigSpec sig, std::string &reg_name)
{
	if (!sig.is_chunk() || sig.as_chunk().wire == NULL)
		return false;

	RTLIL::SigChunk chunk = sig.as_chunk();

	if (reg_wires.count(chunk.wire->name) == 0)
		return false;

	reg_name = id(chunk.wire->name);

	if (sig.size() != chunk.wire->width) {
		if (sig.size() == 1) {
			reg_name += stringf("[%d]",
                                chunk.wire->start_offset +  chunk.offset);
        }
		else if (chunk.wire->upto) {
			reg_name += stringf("[%d:%d]",
                    (chunk.wire->width - (chunk.offset + chunk.width - 1) - 1) +
                        chunk.wire->start_offset,
					(chunk.wire->width - chunk.offset - 1) +
                        chunk.wire->start_offset);
        }
		else {
			reg_name += stringf("[%d:%d]",
                                chunk.wire->start_offset +
                                    chunk.offset + chunk.width - 1,
					            chunk.wire->start_offset +  chunk.offset);
        }
	}

	return true;
}

void
BriscBackend::dump_reg_init(std::ostream& f, SigSpec sig)
{
    Const initval;
    bool gotinit = false;

    for (auto bit : active_sigmap(sig)) {
        if (active_initdata.count(bit)) {
            initval.bits.push_back(active_initdata.at(bit));
            gotinit = true;
        }
        else {
            initval.bits.push_back(State::Sx);
        }
    }

    if (gotinit) {
        f << " = ";
        dump_const(f, initval);
    }
}

void
BriscBackend::dump_conn(
        std::ostream& f,
        std::string indent,
        const SigSpec& left,
        const SigSpec& right)
{
	f << stringf("%s" "assign ", indent.c_str());
	dump_sigspec(f, left);
	f << stringf(" = ");
	dump_sigspec(f, right);
	f << stringf(";\n");
}


